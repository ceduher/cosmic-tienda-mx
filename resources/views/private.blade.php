<h3>IX. Derechos “ARCO” en materia de protección de Datos Personales.</h3>

<span>Los Derechos “ARCO” son un acrónimo que agrupa cuatro derechos que el Titular puede ejercer, en relación con sus Datos Personales:

1. Acceder a los Datos Personales que están sujetos a Tratamiento; es decir, solicitar al Responsable que le permita al Titular conocer qué datos tiene en su posesión.

2. Rectificar cualquiera de los Datos Personales, sujetos a Tratamiento y en posesión del Responsable, que fueran inexactos, incompletos, erróneos y/o imprecisos.

3. Cancelar cualquiera de los Datos Personales, en posesión del Responsable, que legítimamente considere que no están siendo utilizados para cumplir con alguna de las finalidades del Tratamiento y/o que están siendo usados en contravención de la Ley, el Reglamento o de cualquier otra disposición legal aplicable.

4. Oponerse al Tratamiento que efectúa el Responsable, con causa justificada, siempre que el mismo pueda ocasionarle algún daño y/o perjuicio, o que es contrario a la Ley, el Reglamento o cualquier otra disposición legal aplicable.

Para tal efecto, el Titular deberá enviar una solicitud con, al menos, los siguientes requisitos:

1. El nombre del Titular y domicilio u otro medio para comunicarle la respuesta a su solicitud;

2. Los documentos que acrediten la identidad o, en su caso, la representación legal del Titular;

3. La descripción clara y precisa de los Datos Personales respecto de los que se busca ejercer alguno de los derechos antes mencionados;

4. La modalidad por la cual desea obtener respuesta, esto es, por escrito, en documentos electrónicos o cualquier otro medio.

La solicitud de ejercicio de Derechos “ARCO” deberá ser enviada a cualquiera de los medios señalados en la Sección de Contacto. Una vez recibida la solicitud, el Responsable contará con un plazo de quince (15) días naturales para responder al Titular de los Datos Personales, misma que será enviada a través del medio de respuesta que haya designado el Titular. Este plazo podrá ser ampliado previo aviso por una sola vez por un periodo igual, siempre y cuando sea justificada la ampliación. Sin perjuicio de lo anterior, Usted también tendrá derecho de solicitar a FRUTIHOGAR que le remita un Formulario de Derechos ARCO que sea de fácil llenado, para efecto de ejercitar cualquiera de las prerrogativas aquí previstas.</span>

