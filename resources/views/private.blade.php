<h3>I.- Definiciones.</h3>
<span>Salvo los términos definidos en diversas secciones del presente Aviso o en la Ley, los términos que aparezcan con la primera letra en mayúscula, dentro del presente Aviso, tendrán los significados que a continuación se les asignan:

Agregador Bancario.- Se refiere a la empresa especializada que ha sido contratada por el Responsable para procesar los pagos realizados con tarjeta bancaria por el Titular.

Aviso

El presente aviso de privacidad, ya sea expedido en documento físico, electrónico o en cualquier otro formato generado por el Responsable que es puesto a disposición del Titular, previo al Tratamiento de sus Datos Personales.

Datos Personales

Cualquier información concerniente a una persona física identificada o identificable.

Datos Personales Sensibles

Aquellos Datos Personales que afecten a la esfera más íntima de su Titular, o cuya utilización indebida pueda dar origen a discriminación o conlleve un riesgo grave para éste. En particular, se consideran sensibles aquellos que puedan revelar aspectos como origen racial o étnico, estado de salud presente y futuro, información genética, creencias religiosas, filosóficas y morales, afiliación sindical, opiniones políticas o preferencia sexual.

Derechos “ARCO”

Se refiere al conjunto de prerrogativas que tiene el Titular frente al Responsable, con relación a sus Datos Personales, mismas que consisten en síntesis en lo siguiente: el derecho de acceder a sus Datos Personales y a los detalles del tratamiento de los mismos; así como a rectificarlos en caso de ser inexactos; cancelarlos cuando considere que resulten ser excesivos o innecesarios para las finalidades que justificaron su obtención; y oponerse al tratamiento de los mismos para fines específicos.

Encargado

La persona física o moral que sola o conjuntamente con otras trate Datos Personales por cuenta del Responsable. FRUTIHOGAR.- Se refiere a Poder FRUTIHOGAR, S.A.P.I. de C.V.

Ley

Ley Federal de Protección de Datos Personales en Posesión de Particulares.

Medios Digitales

Se refiere, conjuntamente, al Sitio Web y a la Aplicación.

Pedidos

Se refiere a las transacciones celebradas en la Plataforma de FRUTIHOGAR.

Productos

Son las mercaderías que se comercializan en la Plataforma de FRUTIHOGAR.

Reglamento

Reglamento de la Ley Federal de Protección de Datos Personales en Posesión de Particulares.

Responsable

Persona física o moral de carácter privado que lleva a cabo el tratamiento de Datos Personales.

Sección de Contacto

Se refiere conjuntamente a los medios de contacto que FRUTIHOGAR pone a disposición del Titular para los efectos relacionados con el presente Aviso.

Titular

La persona física a quien corresponden los Datos Personales.

Transferencia

Toda comunicación de Datos Personales realizada a persona distinta del Responsable o Encargado del tratamiento.

Tratamiento

La obtención, uso, divulgación o almacenamiento de Datos Personales, por cualquier medio. El uso abarca cualquier acción de acceso, manejo, aprovechamiento, transferencia o disposición de Datos Personales.</span>

